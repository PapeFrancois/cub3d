#include <stddef.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t				i;
	unsigned char		*dest2;
	unsigned const char	*src2;

	if (src == NULL || dest == NULL)
		return (dest);
	dest2 = (unsigned char *) dest;
	src2 = (unsigned const char *)src;
	i = 0;
	while (i < n)
	{
		dest2[i] = src2[i];
		i++;
	}
	return (dest);
}
