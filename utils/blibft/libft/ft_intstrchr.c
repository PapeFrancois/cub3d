#include "libft.h"

int	ft_intstrchr(const char *s, int c)
{
	int		i;

	if (c == 0)
		return (-1);
	i = -1;
	while (s[++i])
	{
		if (s[i] == c % 128)
		{
			return (i);
		}
	}
	return (-1);
}
